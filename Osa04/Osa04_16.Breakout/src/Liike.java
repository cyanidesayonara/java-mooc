
public class Liike {

    private double liikeX;
    private double liikeY;

    public Liike(double xLiike, double yLiike) {
        this.liikeX = xLiike;
        this.liikeY = yLiike;
    }

    public double getLiikeX() {
        return liikeX;
    }

    public void setLiikeX(double xLiike) {
        this.liikeX = xLiike;
    }

    public double getLiikeY() {
        return liikeY;
    }

    public void setLiikeY(double yLiike) {
        this.liikeY = yLiike;
    }

}
